#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (!lst)
		return (NULL);
	if (content)
		lst->content = content;
	else
		lst->content = NULL;
	lst->next = NULL;
	return (lst);
}
